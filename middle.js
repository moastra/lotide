const eqArrays = function(arr1, arr2) {
  if (arr1.length !== arr2.length) {
    return false;
  }
  for (let i = 0; i < arr1.length; i++) {
    if (arr1[i] !== arr2[i]) {
      return false;
    }
  }
  return true;
};

const middle = function(arr) {
  const length = arr.length;
  const mid = Math.floor(length/2);

  if (length <= 2) {
    return [];
  }
  if (length % 2 === 0) {
    return [arr[mid-1], arr[mid]];
  } else {
    return [arr[mid]];
  }
};


